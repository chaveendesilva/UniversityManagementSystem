    //Add Button
    private void Add_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Add_btnActionPerformed

        int month3 = c_m3.getMonth()+1;
        String monthlyExp = txt_totfee.getText();
        String monthlyFee = txt_totexp.getText();
        String profit = txt_totprofit.getText();
       
        
        try {
            
            String q2 = "INSERT INTO profit (Month,MonthlyFee,MonthlyExpenses,MonthlyProfit) values ('"+ month3 +"', '"+ monthlyExp +"', '"+ monthlyFee +"', '"+ profit +"')";
            pst = con.prepareStatement(q2);
            pst.execute();
            
            tableload();
            
            JOptionPane.showMessageDialog(null, "Monthly Profit Inserted!");
            
        } catch (Exception e) {
            
            JOptionPane.showMessageDialog(null, e);
        }
        
    }//GEN-LAST:event_Add_btnActionPerformed

