    private void profit_tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_profit_tableMouseClicked
        
         int r = profit_table.getSelectedRow();
        
        String id = profit_table.getValueAt(r, 0).toString();
        String month = profit_table.getValueAt(r, 1).toString();
        String mfee = profit_table.getValueAt(r, 2).toString();
        String mexp = profit_table.getValueAt(r, 3).toString();
        String mprofit = profit_table.getValueAt(r, 4).toString();
        
        
        
        txt_pid.setText(id);
        c_m3.setMonth(Integer.parseInt(month));
        txt_totfee.setText(mfee);
        txt_totexp.setText(mexp);
        txt_totprofit.setText(mprofit);
        
    }//GEN-LAST:event_profit_tableMouseClicked


