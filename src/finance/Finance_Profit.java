    //Update Button
    private void Update_btn1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Update_btn1ActionPerformed
        
        int p = JOptionPane.showConfirmDialog(null, "Do you really want to UPDATE?");
        
        if(p == 0){
            
        String id = txt_pid.getText();
        String mfee = txt_totfee.getText();
        String mexp = txt_totexp.getText();
        String totprofit = txt_totprofit.getText();
        
        
            
        try {
            DBconnect.connect().createStatement().executeUpdate("UPDATE profit SET  MonthlyFee = '" + mfee + "', MonthlyExpenses = '" + mexp + "', MonthlyProfit = '" + totprofit + "' WHERE ProfitID = '" + id + "' ");
            tableload();
            JOptionPane.showMessageDialog(null, "Update Successfully");

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Update Unsuccessfully");
            JOptionPane.showMessageDialog(rootPane, e);
        }
            
        }     
        
    }//GEN-LAST:event_Update_btn1ActionPerformed

    //Delete Button
    private void Delete_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Delete_btnActionPerformed
        
        int p = JOptionPane.showConfirmDialog(null, "Do you really want to the DELETE");
        
        if (p == 0){
            
             String id = txt_pid.getText();
             
             try{
                 
                 String q = "DELETE FROM profit WHERE ProfitID = '" + id + "'";
                 pst = con.prepareStatement(q);
                 pst.execute();
                 
                 tableload();
                 
                 JOptionPane.showMessageDialog(null, "DELETED Successfull!");
                 
             }catch(Exception e){
                 e.printStackTrace();
             }
            
        }else{
            
            JOptionPane.showMessageDialog(null, "Cancel The Request");
        }
        
        
    }//GEN-LAST:event_Delete_btnActionPerformed

