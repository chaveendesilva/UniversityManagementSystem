    private void Update_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Update_btnActionPerformed

        int p = JOptionPane.showConfirmDialog(null, "If you really want to UPDATE?");

        if (p == 0) {

            String sid = sId_txt.getText();
            String amount = Amount_txt.getText();
            String date = Date_picker.getDateFormatString();
            String bank = BankCombo.getSelectedItem().toString();
            String branch = Branch_txt.getText();
            String img = lbl_imge.getText();

            try {
                DBconnect.connect().createStatement().executeUpdate("UPDATE payment_details SET  student_Id = '" + sid + "', amount = '" + amount + "', bank = '" + bank + "', branch = '" + branch + "',  pay_Date = '" + date + "', pay_slip = '" + img + "' ");
                JOptionPane.showMessageDialog(null, "Update Successfully");

            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Update Unsuccessfully");
                JOptionPane.showMessageDialog(rootPane, e);
            }

            sId_txt.setText(null);
            Amount_txt.setText(null);
            Date_picker.setDateFormatString(null);
            BankCombo.setSelectedItem(null);
            Branch_txt.setText(null);
            lbl_imge.setText(null);

        } else {
            JOptionPane.showMessageDialog(null, "Have a Good Day!");
        }

    }//GEN-LAST:event_Update_btnActionPerformed

