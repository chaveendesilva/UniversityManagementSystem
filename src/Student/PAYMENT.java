    private void Payment_Search() {

        try {
            ResultSet rs = DataBase.DBconnect.connect().createStatement().executeQuery("select student_Id, amount, bank, branch, pay_Date  from payment_details where student_Id ='" + sId_txt.getText() + "'");
            if (rs.next()) {
                sId_txt.setText(rs.getString("student_Id"));
                Amount_txt.setText(rs.getString("amount"));
                BankCombo.setSelectedItem(rs.getString("bank"));
                Branch_txt.setText(rs.getString("branch"));

                Date_picker.setDate(java.sql.Date.valueOf(rs.getString("pay_Date")));
                lbl_imge.setText("pay_slip");

//                Date_picker.setDate(java.sql.Date.valueOf(rs.getString("pay_Date")));
//                Date_picker.setToolTipText("pay_date");

            } else {

                sId_txt.grabFocus();
            }

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

