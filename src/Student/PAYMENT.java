    private void Delete_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Delete_btnActionPerformed

        int p = JOptionPane.showConfirmDialog(null, "If you really want to the DELETE");

        if (p == 0) {

            String sid = sId_txt.getText();
            try {
                DBconnect.connect().createStatement().executeUpdate("DELETE FROM payment_details WHERE student_Id = '" + sid + "'");
                JOptionPane.showMessageDialog(null, "DELETE Successfull");

                sId_txt.setText(null);
                Amount_txt.setText(null);
                BankCombo.setSelectedItem(null);
                Branch_txt.setText(null);
                Date_picker.setToolTipText(null);

            } catch (Exception e) {
                e.printStackTrace();
            }

        } else {
            JOptionPane.showMessageDialog(null, "Cancel The Request");
        }
    }//GEN-LAST:event_Delete_btnActionPerformed

