    private void pdfActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_pdfActionPerformed
        // TODO add your handling code here:
        jButton2ActionPerformed(evt);
        
        String name = ename.getText();
        
        int id_ = Integer.parseInt(eid.getText());
        String id = String.valueOf(id_);
        
        int attend_ = Integer.parseInt(attendance.getText());
        String attend = String.valueOf(attend_);
        
        int otHrs_ = Integer.parseInt(ot.getText());
        String otHrs = String.valueOf(otHrs_);
        
        int halfdays_ = Integer.parseInt(hdayHrs.getText());
        String halfdays = String.valueOf(halfdays_);
        
        double basic_ = Double.parseDouble(bsal.getText());
        String basic = String.valueOf(basic_);
        
        double grossSal = Double.parseDouble(gross.getText());
        
        try {
            Document doc = new Document();
            PdfWriter docwrite = PdfWriter.getInstance(doc, new FileOutputStream(name + ".pdf"));
            
            doc.open();
            
            doc.add(new Paragraph("Employee pay slip", FontFactory.getFont(FontFactory.TIMES_BOLD, 20)));
            doc.add(new Paragraph("----------------------------------------------------------------------", FontFactory.getFont(FontFactory.TIMES_BOLD, 20)));
            doc.add(new Paragraph("Date : 09-27-2019", FontFactory.getFont(FontFactory.TIMES, 14)));
            doc.add(new Paragraph("----------------------------------------------------------------------", FontFactory.getFont(FontFactory.TIMES_BOLD, 20)));
            doc.add(new Paragraph("Employee name : " + name, FontFactory.getFont(FontFactory.TIMES, 14)));
            doc.add(new Paragraph("Employee ID : " + id, FontFactory.getFont(FontFactory.TIMES, 14)));
            doc.add(new Paragraph("Attendance for this month : " + attend, FontFactory.getFont(FontFactory.TIMES, 14)));
            doc.add(new Paragraph("Over Time hours : " + otHrs, FontFactory.getFont(FontFactory.TIMES, 14)));
            doc.add(new Paragraph("Half Day and Short Leave Hours : " + halfdays, FontFactory.getFont(FontFactory.TIMES, 14)));
            doc.add(new Paragraph("Basic Salary : " + basic, FontFactory.getFont(FontFactory.TIMES, 14)));
            doc.add(new Paragraph("----------------------------------------------------------------------", FontFactory.getFont(FontFactory.TIMES_BOLD, 20)));
            doc.add(new Paragraph("Gross Salary for this month : " + grossSal, FontFactory.getFont(FontFactory.TIMES_BOLD, 14)));
            
            doc.close();
            docwrite.close();
            
            JOptionPane.showMessageDialog(rootPane, "Report generated successfully.");
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_pdfActionPerformed


