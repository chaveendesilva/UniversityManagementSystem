    private void editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editActionPerformed

        String eimage = jLabel1.getText();
        String ename = username.getText();
        String epassword = String.valueOf(jPasswordField1);
        String echange = String.valueOf(ChangeP);

        try {
            DBconnect.connect().createStatement().executeUpdate("UPDATE EditUser SET "
                    + "image = " + eimage
                    + "cpassword = " + epassword
                    + "changep = " + echange
                    + " WHERE username = " + ename);

        } catch (Exception e) {
            e.printStackTrace();
        }


    }//GEN-LAST:event_editActionPerformed

