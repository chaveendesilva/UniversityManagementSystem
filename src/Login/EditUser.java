    private void cImageActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cImageActionPerformed

        int x = jFileChooser2.showDialog(this, "Open");

        if (x == JFileChooser.APPROVE_OPTION) {
            Toolkit tk = Toolkit.getDefaultToolkit();
            Image img = tk.getImage(jFileChooser2.getSelectedFile().getAbsolutePath());
            Image imgScale = img.getScaledInstance(jLabel1.WIDTH, jLabel1.HEIGHT, Image.SCALE_DEFAULT);
            jLabel1.setIcon(new ImageIcon());
        } else {
            jLabel1.setIcon(new ImageIcon());
        }

    }//GEN-LAST:event_cImageActionPerformed


